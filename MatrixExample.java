public class MatrixExample {
    public static void main(String[] args) {
        int[][] matrix = {
            {1, 2, 3, 9},
            {4, 5, 6, 8},
            {7, 8, 9, 7}
        };

        System.out.println("Number of rows: " + matrix.length);
        System.out.println("Number of columns: " + matrix[1].length);
    }
}